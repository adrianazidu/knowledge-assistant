#  APPROACH B — MINE FROM GITLAB ISSUES AND MRs
#  Real conversations from your team = highest quality training data.
#  Issues with accepted solutions become instruction/output pairs.
#  MRs with review comments become code review training examples.
# ════════════════════════════════════════════════════════════════════════

def mine_gitlab(max_issues: int = 100, max_mrs: int = 50) -> list[dict]:
    """
    Extract training examples from real GitLab issues and MRs.

    For issues: if an issue has a clear resolution in the comments,
    the issue body = instruction+input, the resolution comment = output.

    For MRs: the MR description = instruction+input,
    the key review comment that explains the change = output.
    """
    try:
        import gitlab
    except ImportError:
        print("  ❌  pip install python-gitlab")
        return []

    import config
    if not config.GITLAB_TOKEN:
        print("  ❌  GITLAB_TOKEN not set in .env")
        return []

    gl      = gitlab.Gitlab(config.GITLAB_URL, private_token=config.GITLAB_TOKEN)
    project = gl.projects.get(config.GITLAB_PROJECT_ID)
    examples = []

    print(f"\n  Mining GitLab: {project.name_with_namespace}")

    # ── Mine issues ───────────────────────────────────────────────────────────
    print(f"  Mining issues (max {max_issues})...")
    issues = project.issues.list(state="closed", per_page=50, get_all=False)

    for issue in issues[:max_issues]:
        if not issue.description or len(issue.description) < 50:
            continue

        notes     = issue.notes.list(get_all=True)
        user_notes = [n for n in notes if not n.system and len(n.body) > 80]

        if not user_notes:
            continue

        # Use the last substantive comment as the "answer"
        # In a real closed issue, this is usually the resolution
        resolution = user_notes[-1].body

        examples.append({
            "instruction": f"A developer opened an issue: {issue.title}",
            "input":       issue.description[:500],   # cap length
            "output":      resolution[:800],
            "metadata":    {
                "source":   "gitlab_issue",
                "issue_id": issue.iid,
                "url":      issue.web_url,
            }
        })

    print(f"    → {len(examples)} issue examples")

    # ── Mine MRs ──────────────────────────────────────────────────────────────
    mr_examples = []
    print(f"  Mining MRs (max {max_mrs})...")
    mrs = project.mergerequests.list(state="merged", per_page=50, get_all=False)

    for mr in mrs[:max_mrs]:
        if not mr.description or len(mr.description) < 100:
            continue

        notes      = mr.notes.list(get_all=True)
        user_notes = [n for n in notes if not n.system and len(n.body) > 60]

        if not user_notes:
            continue

        # Best review comment = longest one (usually most substantive)
        best_comment = max(user_notes, key=lambda n: len(n.body))

        mr_examples.append({
            "instruction": f"Review this merge request: {mr.title}",
            "input":       mr.description[:500],
            "output":      best_comment.body[:800],
            "metadata":    {
                "source": "gitlab_mr",
                "mr_id":  mr.iid,
                "url":    mr.web_url,
            }
        })

    print(f"    → {len(mr_examples)} MR examples")
    examples.extend(mr_examples)
    return examples


