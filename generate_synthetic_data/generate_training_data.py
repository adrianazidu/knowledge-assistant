def load_existing() -> list[dict]:
    if not os.path.exists(OUTPUT_PATH):
        return []
    with open(OUTPUT_PATH) as f:
        return json.load(f)

def save(examples: list[dict]):
    os.makedirs(os.path.dirname(OUTPUT_PATH), exist_ok=True)
    with open(OUTPUT_PATH, "w") as f:
        json.dump(examples, f, indent=2)
    print(f"  Saved {len(examples)} total examples → {OUTPUT_PATH}")

def add_examples(new_examples: list[dict], label: str):
    existing = load_existing()
    before   = len(existing)
    existing.extend(new_examples)
    save(existing)
    print(f"  Added {len(new_examples)} {label} examples ({before} → {len(existing)} total)")
    return existing


