# ── Goal 3: Distillation ──────────────────────────────────────────────────────
# Generate GPT-4o quality answers to YOUR specific domain questions.
# Fine-tune a smaller model on these — it learns to give GPT-4o quality
# answers on your domain at a fraction of the inference cost.

DISTILLATION_SEED_QUESTIONS = [
    "What's the difference between optimistic and pessimistic locking? When should I use each?",
    "Our Postgres query is slow after adding 10M rows. What should I check first?",
    "Should I use a message queue or direct API calls between these two services?",
    "How do I implement rate limiting without a cache like Redis?",
    "What's the safest way to do a zero-downtime database migration?",
    "When should I use a transaction vs just letting operations run independently?",
    "How do I debug a memory leak in a Python service?",
    "What's the right way to handle pagination for an API that returns millions of records?",
    "Our CI pipeline takes 40 minutes. What are the most impactful ways to speed it up?",
    "How should I structure logging so I can actually debug production issues?",
]

def generate_distillation_examples(questions: list[str] = None) -> list[dict]:
    """
    Send each question to GPT-4o and use its answer as the training output.
    Fine-tuning a smaller model on these teaches it GPT-4o reasoning patterns
    for your specific domain.
    """
    questions = questions or DISTILLATION_SEED_QUESTIONS
    examples  = []

    print(f"\n  Generating {len(questions)} distillation examples via GPT-4o...")

    for i, question in enumerate(questions, 1):
        print(f"  [{i}/{len(questions)}] {question[:60]}...")

        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[
                {"role": "system", "content":
                 "You are a senior backend engineer. Give specific, opinionated, actionable answers. "
                 "Show reasoning steps. Mention trade-offs. Avoid generic advice."},
                {"role": "user", "content": question}
            ],
            temperature=0.3,
        )
        answer = response.choices[0].message.content.strip()

        examples.append({
            "instruction": question,
            "input":       "",
            "output":      answer,
            "metadata":    {"source": "distillation_gpt4o"}
        })

    return examples


