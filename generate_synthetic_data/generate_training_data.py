SYNTHETIC_PROMPT = """You are generating training data for an AI technical assistant
that helps developers on a backend engineering team.

Here are examples of the style, tone, and depth we want:

{examples}

Generate {count} more examples in EXACTLY the same format.
Cover a variety of technical topics: debugging, architecture decisions,
code review, database queries, API design, performance, deployment, security.

Make the outputs sound like a knowledgeable senior engineer — specific,
actionable, and honest about trade-offs. No generic advice.

Return ONLY a valid JSON array of objects with keys:
"instruction", "input", "output"
No markdown, no explanation, just the JSON array."""

def generate_synthetic(count: int = 50) -> list[dict]:
    print(f"\n  Generating {count} synthetic examples via GPT-4o...")

    # Generate in batches of 20 to avoid hitting token limits
    batch_size = 20
    all_examples = []

    for batch_start in range(0, count, batch_size):
        batch_count = min(batch_size, count - batch_start)
        print(f"  Batch {batch_start//batch_size + 1}: generating {batch_count} examples...")

        examples_text = json.dumps(SEED_EXAMPLES, indent=2)
        prompt = SYNTHETIC_PROMPT.format(examples=examples_text, count=batch_count)

        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[{"role": "user", "content": prompt}],
            temperature=0.8,    # some creativity for variety
            response_format={"type": "json_object"},
        )

        raw = response.choices[0].message.content
        # GPT-4o with json_object sometimes wraps in a key
        parsed = json.loads(raw)
        if isinstance(parsed, list):
            batch = parsed
        else:
            # find the list inside the object
            batch = next((v for v in parsed.values() if isinstance(v, list)), [])

        # Validate and clean
        valid = []
        for ex in batch:
            if all(k in ex for k in ("instruction", "output")):
                if not ex.get("input"):
                    ex["input"] = ""
                valid.append(ex)

        all_examples.extend(valid)
        print(f"    → {len(valid)} valid examples")

    return all_examples


