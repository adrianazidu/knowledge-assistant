def main():
    parser = argparse.ArgumentParser(description="Generate fine-tuning training data")
    parser.add_argument("--synthetic",     action="store_true", help="Generate via GPT-4o synthetic generation")
    parser.add_argument("--count",         type=int, default=50, help="Number of synthetic examples to generate")
    parser.add_argument("--gitlab",        action="store_true", help="Mine from GitLab issues and MRs")
    parser.add_argument("--goals",         action="store_true", help="Add examples for all 4 fine-tuning goals")
    parser.add_argument("--distillation",  action="store_true", help="Generate distillation examples only")
    parser.add_argument("--all",           action="store_true", help="Run everything")
    parser.add_argument("--stats",         action="store_true", help="Show current dataset stats")
    args = parser.parse_args()

    if args.stats:
        existing = load_existing()
        print(f"\n  Total examples: {len(existing)}")
        sources = {}
        for ex in existing:
            src = ex.get("metadata", {}).get("source", "manual")
            sources[src] = sources.get(src, 0) + 1
        for src, count in sorted(sources.items()):
            print(f"    {src:<30} {count}")
        print()
        return

    if args.all or args.synthetic:
        examples = generate_synthetic(count=args.count)
        add_examples(examples, "synthetic")

    if args.all or args.gitlab:
        examples = mine_gitlab()
        add_examples(examples, "gitlab")

    if args.all or args.goals:
        print("\n  Adding structured output examples...")
        add_examples(STRUCTURED_OUTPUT_EXAMPLES, "structured_output")

        print("\n  Adding reasoning pattern examples...")
        add_examples(REASONING_PATTERN_EXAMPLES, "reasoning_pattern")

        print("\n  Adding safety/refusal examples...")
        add_examples(REFUSAL_EXAMPLES, "safety_refusal")

    if args.all or args.distillation:
        examples = generate_distillation_examples()
        add_examples(examples, "distillation")

    if not any([args.synthetic, args.gitlab, args.goals, args.distillation, args.all, args.stats]):
        parser.print_help()


if __name__ == "__main__":
    main()
